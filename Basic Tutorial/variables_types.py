# python supports integers, floating point, and complex numbers
myint = 7
print(myint)
myfloat = 7.0
print(myfloat)
myfloat = float(7) #typecasting
print(myfloat)

# handling strings
mystring = 'hello'
print(mystring)
mystring = "hello"
print(mystring)

